// Write a Java program to convert all the characters in a string to lowercase.
// Sample Output:
// Original String: The Quick BroWn FoX!
// String in lowercase: the quick brown fox!

public class String_To_Lower_Case {

    public static void main(String[] args) {
    
        System.out.println("Name: Tayyab Ali. RollNo: 2024S-BSET-003.");

        String originalString = "The Quick BroWn FoX!";
        
        String lowercaseString = originalString.toLowerCase();
        
        System.out.println("Original String: " + originalString);
        
        System.out.println("String in lowercase: " + lowercaseString);

    }
    
}